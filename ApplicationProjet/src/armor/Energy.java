package armor;

public class Energy {
	String name;
	int value;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	@Override
	public String toString() {
		return "Energy [name=" + name + ", value=" + value + "]";
	}
}
